#To check if a number isn't equal to another number, we use the inequality operator !=
print(1!=10)

result = 1 != 2
print(result)

#Variables can store the result of equality comparisons too
result = 1==2
print(result)

#Compare values with variables and variables with other variables with ==
one = 1
two = 2
print(one == two)
print(one != two)
